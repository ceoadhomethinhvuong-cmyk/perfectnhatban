                            function setupPopup(openSelector, popupId, hideSticky = false) {
                                const openBtns = document.querySelectorAll(openSelector);
                                const popup = document.querySelector(popupId);
                                const closeBtns = popup?.querySelectorAll(
                                    ".tda-popup-close, .tda-product-popup-close"
                                );
                                const stickyBar = document.getElementById("tdaStickyBar");

                                // Hàm mở popup
                                function openPopup(e) {
                                    if (e) {
                                        e.preventDefault();
                                        e.stopPropagation();
                                    }
                                    if (popup) {
                                        popup.style.display = "flex";
                                        if (hideSticky && stickyBar) stickyBar.style.display = "none";
                                    }
                                }

                                // Hàm đóng popup
                                function closePopup() {
                                    if (popup) {
                                        popup.style.display = "none";
                                        if (hideSticky && stickyBar) stickyBar.style.display = "";
                                    }
                                }

                                // Gán event cho tất cả các nút mở popup
                                if (openBtns && openBtns.length > 0 && popup) {
                                    openBtns.forEach(function(openBtn) {
                                        // Nếu openBtn là container, tìm link bên trong
                                        const clickableElement = openBtn.tagName === 'A' || openBtn
                                            .tagName === 'BUTTON' ?
                                            openBtn :
                                            openBtn.querySelector('a, button') || openBtn;

                                        clickableElement.addEventListener("click", openPopup);

                                        // Nếu là container, cũng gán event cho chính nó
                                        if (openBtn !== clickableElement) {
                                            openBtn.addEventListener("click", function(e) {
                                                // Chỉ xử lý nếu click trực tiếp vào container, không phải vào element con
                                                if (e.target === openBtn) {
                                                    openPopup(e);
                                                }
                                            });
                                        }
                                    });
                                }

                                // Gán event cho nút đóng popup
                                if (closeBtns && closeBtns.length > 0) {
                                    closeBtns.forEach((btn) => {
                                        btn.addEventListener("click", closePopup);
                                    });
                                }

                                // Đóng popup khi click vào overlay
                                if (popup) {
                                    popup.addEventListener("click", function(e) {
                                        if (e.target === popup) {
                                            closePopup();
                                        }
                                    });
                                }
                            }

